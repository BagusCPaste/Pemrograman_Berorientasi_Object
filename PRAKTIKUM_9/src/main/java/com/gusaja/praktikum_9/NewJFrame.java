    void hitung() {
        ptugas = 0.2 * tugas;
        puts = 0.35 * uts;
        puas = 0.45 * uas;
        akhir = ptugas + puts + puas;
    }

    String getNilHuruf() {
        if (akhir >= 85) {
            nHuruf = "A";
        } else if (akhir >= 70 && akhir < 85) {
            nHuruf = "B";
        } else if (akhir >= 65 && akhir < 70) {
            nHuruf = "C";
        } else {
            nHuruf = "D";
        }
        return nHuruf;
    }

    String getPredikat() {
        switch (nHuruf) {
            case "A":
                predikat = "Apik";
                break;
            case "B":
                predikat = "Bagus";
                break;
            case "C":
                predikat = "Jelek";
                break;
            case "D":
                predikat = "Dablek";
                break;
            default:
                predikat = "-";
        }
        return predikat;
    }

