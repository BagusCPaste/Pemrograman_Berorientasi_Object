    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
            if(nama.getText().trim().isEmpty() || nim.getText().trim().isEmpty() || ntugas.getText().trim().isEmpty()|| nuts.getText().trim().isEmpty() || nuas.getText().trim().isEmpty()){
                JOptionPane.showMessageDialog(null, "Maaf Data Harus Lengkap");
            }else{
                tabel.addRow(new Object[]
                {nama.getText(),nim.getText(),ntugas.getText(),nuts.getText(),nuas.getText(),nakhir.getText(),npredikat.getText(),nhuruf.getText()});
                table.setModel(tabel);
                nama.setText("");
                nim.setText("");
                ntugas.setText("");
                nuts.setText("");
                nuas.setText("");
                natugas.setText("");
                nauts.setText("");
                nauas.setText("");
                nakhir.setText("");
                npredikat.setText("");
                nhuruf.setText("");
                JOptionPane.showMessageDialog(null,"Data Berhasil Ditambah");
            }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void ntugasFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_ntugasFocusLost
        String nntugas = ntugas.getText();
        try{
            tugas = Double.parseDouble(nntugas);
            ptugas = 0.2 * tugas;
            natugas.setText(""+ptugas);
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Maaf Data Anda Tidak Valid");
        }
    }//GEN-LAST:event_ntugasFocusLost

    private void nutsFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_nutsFocusLost
       try{
            String nnuts = nuts.getText();
            uts = Double.parseDouble(nnuts);
            puts = 0.35 * uts;
            nauts.setText(""+puts);
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Maaf Data Anda Tidak Valid");
        }
    }//GEN-LAST:event_nutsFocusLost

    private void nuasFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_nuasFocusLost
        try{
            String nnuas = nuas.getText();
            uas = Double.parseDouble(nnuas);
            puas = 0.45 * uas;
            nauas.setText(""+puas);
            akhir = ptugas + puts + puas;
            nakhir.setText(""+akhir);
            nhuruf.setText(""+getNilHuruf());
            npredikat.setText(""+getPredikat());
        }catch(Exception e){
                JOptionPane.showMessageDialog(null, "Maaf Data Anda Tidak Valid");
        }
    }//GEN-LAST:event_nuasFocusLost

